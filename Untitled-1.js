// Function to check if a number is even or odd
function checkEvenOrOdd(n) {
  if (n % 2 === 0) {
    return "Even";
  } else {
    return "Odd";
  }
}

// Example usage
let userInput = prompt("Enter a number:");
let result = checkEvenOrOdd(parseInt(userInput));

console.log(`The number is ${result}.`);
